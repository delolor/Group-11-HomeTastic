    private void jDoneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDoneButtonActionPerformed
        int row = TableReport.getSelectedRow();
        String cell = TableReport.getModel().getValueAt(row, 0).toString();
        String query = "UPDATE `hometastic`.`report` SET `REPORT_STATUS` = 'DONE' WHERE `REPORT_ID` = " + cell;
        try{
            Connection connection = getConnection();
            PreparedStatement ps = connection.prepareStatement(query);
            ps.execute();
            JOptionPane.showMessageDialog(null, "Marked as Done!");
            update_table();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jDoneButtonActionPerformed

