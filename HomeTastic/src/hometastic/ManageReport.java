    public void update_table(){
        String query = "select * from report";
        try{
            Connection connection = getConnection();
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            TableReport.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void show_report(){
        ArrayList<Report> list = getreportList();
        DefaultTableModel model = (DefaultTableModel)TableReport.getModel();
        Object[] row = new Object[4];
        for(int i = 0; i < list.size(); i++){
            row[0] = list.get(i).getReportID();
            row[1] = list.get(i).getReportType();
            row[2] = list.get(i).getReportDesc();
            row[3] = list.get(i).getReportStatus();
            
            model.addRow(row);
        }
    }
    
    

